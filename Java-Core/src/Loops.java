
public class Loops {
    public static void main(String[] args) {

        int number = 23;
        while(number < 30) {
            System.out.println("Okay" + " " + number);
            number = number + 2;
        }

        for(int i = 0; i < 5; i++) {
            System.out.println(i);
        }

    }
}
